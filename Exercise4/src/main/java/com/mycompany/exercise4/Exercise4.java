public class Exercise4 {

  public static void main(String[] args) {
    double fahrenheit;
    double celsius;

    celsius = 289;
    fahrenheit = (celsius * 9 / 5) + 32;
    System.out.println("For Celsius = 289, Fahrenheit = " + fahrenheit);

    celsius = 400;
    fahrenheit = (celsius * 9 / 5) + 32;
    System.out.println("For Celsius = 400, Fahrenheit = " + fahrenheit);

    celsius = -36;
    fahrenheit = (celsius * 9 / 5) + 32;
    System.out.println("For Celsius = -36, Fahrenheit = " + fahrenheit);

    celsius = -180;
    fahrenheit = (celsius * 9 / 5) + 32;
    System.out.println("For Celsius = -180, Fahrenheit = " + fahrenheit);
}

}
